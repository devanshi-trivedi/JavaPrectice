package com.Selfstudy;

public class calculator {
	
	int num1 = 10;
	int num2 = 5;
	
	
	public int add (int num1, int num2) {
		
		return num1 + num2;
	}
	
	public int multiply (int num1,int num2) {
		
		return num1*num2;
	}
	
	public int subtract (int num1,int num2) {
		return num1 - num2;
	}
	public double division (double num1, double num2) {
		
		return num1/num2;
		
	}

}


